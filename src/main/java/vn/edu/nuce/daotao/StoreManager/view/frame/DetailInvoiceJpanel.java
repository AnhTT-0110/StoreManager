    private void cbbProductItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cbbProductItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            setValuePriceAndAmount();
            txtDcash.setText("");
            txtQtty.setText("");
        }

    }//GEN-LAST:event_cbbProductItemStateChanged

    private void setValuePriceAndAmount() {
        String[] productSelected = cbbProduct.getSelectedItem().toString().split("_");
        String codeCusConvert = productSelected[0].trim();
        String price = productController.getProductResponseById(codeCusConvert).getPrice();
        String amount = productController.getProductResponseById(codeCusConvert).getQuantity();
        txtAmout.setText(amount);
        txtPrice.setText(price);
    }

    private void txtQttyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQttyKeyReleased
        if (!txtQtty.getText().isEmpty()) {
            Double dcash = Double.valueOf(txtPrice.getText()) * Integer.valueOf(txtQtty.getText());
            txtDcash.setText(String.format("%,.2f", dcash));
        }
    }//GEN-LAST:event_txtQttyKeyReleased

